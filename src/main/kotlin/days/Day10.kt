package days

class Day10 : Day(10) {

    override fun partOne(): Any {
        val input = inputList.map { it.toInt() }.sorted()
        var d1 = 1
        var d3 = 1
        for (i in 0..input.size-2) {
            when (input[i+1] - input[i]) {
                1 -> d1++
                3 -> d3++
            }
        }
        return d1*d3
    }

    override fun partTwo(): Any {
        val input = inputList.map { it.toInt() }.sorted()
        var d: Long = 1
        for (i in 0..input.size-4) {
            when (input[i+1] - input[i]) {
                1,2,3 -> d *= 2
            }
            when (input[i+2] - input[i]) {
                2,3 -> d *= 2
            }
            when (input[i+3] - input[i]) {
                3 -> d *= 2
            }
        }
        return d
    }
}

